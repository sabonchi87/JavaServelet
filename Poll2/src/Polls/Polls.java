package Polls;

public class Polls {

	int id;
	String name;
	String a1;
	String a2;
	String a3;
	
	
	
	public Polls(int id, String name, String a1, String a2, String a3) {
		super();
		this.id = id;
		this.name = name;
		this.a1 = a1;
		this.a2 = a2;
		this.a3 = a3;
	}
	
	
	
	public int getId() {
		return id;
	}



	public void setId(int id) {
		this.id = id;
	}



	public String getName() {
		return name;
	}



	public void setName(String name) {
		this.name = name;
	}



	public String getA1() {
		return a1;
	}



	public void setA1(String a1) {
		this.a1 = a1;
	}



	public String getA2() {
		return a2;
	}



	public void setA2(String a2) {
		this.a2 = a2;
	}



	public String getA3() {
		return a3;
	}



	public void setA3(String a3) {
		this.a3 = a3;
	}


	
	
	
	
	
	
	
	
	
	
}
