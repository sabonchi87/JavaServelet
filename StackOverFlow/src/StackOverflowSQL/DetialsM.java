package StackOverflowSQL;

public class DetialsM {
	
	int id;
	 String name;
	 int q_id;
	 
	 
	public DetialsM(int id, String name, int q_id) {
		super();
		this.id = id;
		this.name = name;
		this.q_id = q_id;
	}
	
	
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getQ_id() {
		return q_id;
	}
	public void setQ_id(int q_id) {
		this.q_id = q_id;
	}
	 
	 
	
	
	
	 
	 
	

}
