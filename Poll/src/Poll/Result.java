package Poll;

public class Result {
	
int id;
int a;
int b;
int c;
int c_id;
public Result(int id, int a, int b, int c, int a_id) {
	super();
	this.id = id;
	this.a = a;
	this.b = b;
	this.c = c;
	this.c_id = a_id;
}
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public int getA() {
	return a;
}
public void setA(int a) {
	this.a = a;
}
public int getB() {
	return b;
}
public void setB(int b) {
	this.b = b;
}
public int getC() {
	return c;
}
public void setC(int c) {
	this.c = c;
}
public int geta_id() {
	return c_id;
}
public void seta_id(int c_id) {
	this.c_id = c_id;
}








}
